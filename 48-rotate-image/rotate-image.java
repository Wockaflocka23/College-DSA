class Solution {

    // Transpose the matrix
    private void transpose(int[][] matrix) {
        int n = matrix.length;

        for (int i = 0; i < n; i++) {
            for (int j = i + 1; j < n; j++) {
                // Swap matrix[i][j] and matrix[j][i]
                int temp = matrix[i][j];
                matrix[i][j] = matrix[j][i];
                matrix[j][i] = temp;
            }
        }
    }

    // Reverse each row
    private void reverseRows(int[][] matrix) {
        int n = matrix.length;

        for (int i = 0; i < n; i++) {
            int left = 0, right = n - 1;
            while (left < right) {
                int temp = matrix[i][left];
                matrix[i][left] = matrix[i][right];
                matrix[i][right] = temp;
                left++;
                right--;
            }
        }
    }

    // Main method to rotate matrix by 90 degrees clockwise
    public void rotate(int[][] matrix) {
        transpose(matrix);
        reverseRows(matrix);
    }
}